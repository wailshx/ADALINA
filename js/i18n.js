    var dict = { fr: FR, ar: AR };

    function getLang() {
        return localStorage.getItem('adalina-lang') || 'fr';
    }

    function setLang(lang) {
        localStorage.setItem('adalina-lang', lang);
        document.documentElement.lang = lang;
        document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
        applyTranslations(lang);
        updateLangToggle(lang);
    }

    function t(key) {
        var lang = getLang();
        return (dict[lang] && dict[lang][key]) || (dict.fr && dict.fr[key]) || key;
    }

    function applyTranslations(lang) {
        var d = dict[lang] || dict.fr;
        document.querySelectorAll('[data-i18n]').forEach(function(el) {
            var key = el.getAttribute('data-i18n');
            if (d[key]) el.textContent = d[key];
        });
        document.querySelectorAll('[data-i18n-placeholder]').forEach(function(el) {
            var key = el.getAttribute('data-i18n-placeholder');
            if (d[key]) el.placeholder = d[key];
        });
    }

    function updateLangToggle(lang) {
        var btn = document.getElementById('lang-toggle');
        if (btn) btn.textContent = lang === 'fr' ? '\u0639\u0631\u0628\u064a' : 'FR';
    }

    function toggleLang() {
        setLang(getLang() === 'fr' ? 'ar' : 'fr');
    }

